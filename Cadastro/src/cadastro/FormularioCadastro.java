    private void jBConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConsultarActionPerformed
        // TODO add your handling code here:
        String nomeInformacoes;
        String enderecoInformacoes;
        String cpfInformacoes;
        String telefoneInformacoes;
        String rgInformacoes;
        String emailInformacoes;

        nomeInformacoes = informacoes.retorna_nome();
        enderecoInformacoes = informacoes.retorna_endereco();
        cpfInformacoes = informacoes.retorna_cpf();
        telefoneInformacoes = informacoes.retorna_telefone();
        rgInformacoes = informacoes.retorna_rg();
        emailInformacoes = informacoes.retorna_email();

        JOptionPane.showMessageDialog(null, "  Nome:  " + nomeInformacoes + "  Endereco:  " + enderecoInformacoes +
            "  cpf:  "  + cpfInformacoes + "  Telefone:  " + telefoneInformacoes + "  rg:  "  + rgInformacoes + "  email:  " +
            emailInformacoes
        );

    }//GEN-LAST:event_jBConsultarActionPerformed

